CREATE DATABASE EX14

USE EX14
-- O imposto = (RENDA * ALI) - DEDUCAO
CREATE OR ALTER FUNCTION SP_IMPOSTO_RENDA (@RENDA NUMERIC(10,2))
RETURNS NUMERIC(10,2)
AS
BEGIN
	RETURN IIF(@RENDA > 1372.81,(IIF(@RENDA > 2743.25, (@RENDA * 0.275) - 548.82, (@RENDA * 0.15) - 205.92)), 0)
END

